defmodule MeanToEndTest do
  use ExUnit.Case

  test "it should calculate correct median after inserts" do
    {:ok, socket} = :gen_tcp.connect(~c"localhost", 5003, mode: :binary, active: false)

    assert :gen_tcp.send(socket, <<0x49, 0x00, 0x00, 0x30, 0x39, 0x00, 0x00, 0x00, 0x65>>) == :ok
    assert :gen_tcp.send(socket, <<0x49, 0x00, 0x00, 0x30, 0x3A, 0x00, 0x00, 0x00, 0x66>>) == :ok
    assert :gen_tcp.send(socket, <<0x49, 0x00, 0x00, 0x30, 0x3B, 0x00, 0x00, 0x00, 0x64>>) == :ok
    assert :gen_tcp.send(socket, <<0x49, 0x00, 0x00, 0xA0, 0x00, 0x00, 0x00, 0x00, 0x05>>) == :ok
    assert :gen_tcp.send(socket, <<0x51, 0x00, 0x00, 0x30, 0x00, 0x00, 0x00, 0x40, 0x00>>) == :ok

    :gen_tcp.shutdown(socket, :write)

    assert :gen_tcp.recv(socket, 0) == {:ok, <<101::32-signed-integer>>}
  end
end
